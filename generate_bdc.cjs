const bdcBranches = [];
for (const [region, districts] of Object.entries(bdcData)) {
  const isGov = ["القاهرة", "الجيزة", "الإسكندرية"].includes(region);
  for (const [district, locations] of Object.entries(districts)) {
    for (const loc of locations) {
      if (bdcBranches.length >= 260) break;
      let branchName = loc.includes("فرع") ? loc : `فرع ${loc}`;
      let gov = isGov ? region : district;
      if (region === "الوجه البحري" || region === "القناة وسيناء" || region === "الصعيد") {
         gov = district; // Use the district key as governorate
      } else {
         gov = region;
      }
      
      let actualDistrict = district;
      if (!isGov) actualDistrict = loc;
      
      bdcBranches.push({
        branchName: branchName,
        governorate: gov,
        district: actualDistrict,
        detailedAddress: `${branchName}، ${actualDistrict}، ${gov}`,
        phone: "16990",
        googleMapsLink: generateMapLink("بنك القاهرة", branchName, gov)
      });
    }
  }
}

