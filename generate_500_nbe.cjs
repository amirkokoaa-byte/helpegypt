const branches = [];
let idCounter = 1;

const addBranches = (gov, districts, streets, count, govPrefix) => {
  for (let i = 0; i < count; i++) {
    const district = districts[i % districts.length];
    const street = streets[i % streets.length];
    let branchName = `فرع ${district}`;
    if (i >= districts.length) {
      branchName += ` (${street})`;
    }
    if (i >= districts.length * streets.length) {
      branchName += ` ${Math.floor(i / (districts.length * streets.length)) + 1}`;
    }
    
    branches.push({
      branchName,
      governorate: gov,
      district: district,
      detailedAddress: `${street}، ${district}، ${gov}`,
      phone: "19623",
      googleMapsLink: generateMapLink(branchName, gov)
    });
  }
};

