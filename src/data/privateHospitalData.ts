// Generate exactly 250 private/investment hospitals
export function generatePrivateHospitals(): Hospital[] {
  // Map basePrivateHospitals to ensure they all have regionAr and regionEn
  const result: Hospital[] = basePrivateHospitals.map(h => ({
    ...h,
    regionAr: h.regionAr || h.districtAr,
    regionEn: h.regionEn || h.districtEn
  }));
  const targetCount = 250;
  
  let idCounter = 1;
  const usedIds = new Set(basePrivateHospitals.map(h => h.id));
  
  while (result.length < targetCount) {
    const isCairo = idCounter % 2 === 0;
    const govAr = isCairo ? 'القاهرة' : 'الجيزة';
    const govEn = isCairo ? 'Cairo' : 'Giza';
    const districtList = isCairo ? cairoDistricts : gizaDistricts;
    const districtObj = districtList[idCounter % districtList.length];
    
    const nameTemplate = nameTemplates[idCounter % nameTemplates.length];
    const nameAr = `${nameTemplate.ar}${districtObj.ar} (خاص رقم ${idCounter})`;
    const nameEn = `${nameTemplate.en}${districtObj.en} (Private No. ${idCounter})`;
    
    const specialtiesObj = privateSpecialties[idCounter % privateSpecialties.length];
    
    // Physical address
    const addressAr = `شارع العيادات متفرع من الطريق التجاري الرئيسي، ${districtObj.ar}، ${govAr} بجوار مجمع البنوك`;
    const addressEn = `Clinics St, Off Main Commercial Axis, ${districtObj.en}, ${govEn} near Bank Complex`;
    
    // Phone numbers (02-2xxxxxxx for Cairo, 02-3xxxxxxx for Giza)
    const startDigit = isCairo ? '2' : '3';
    const randomDigits = String(3350000 + (idCounter * 211) % 1500000);
    const phone = `02-${startDigit}${randomDigits}`;
    
    // 25% chance of hotline
    const hasHotline = idCounter % 4 === 0;
    const hotlineList = ['19070', '19595', '19885', '16370', '19474', '19555', '16781', '19412', '16923', '15414'];
    const hotline = hasHotline ? hotlineList[idCounter % hotlineList.length] : undefined;
    
    const uniqueId = `pvt_gen_${idCounter}`;
    
    if (!usedIds.has(uniqueId)) {
      result.push({
        id: uniqueId,
        nameAr,
        nameEn,
        classificationAr: idCounter % 3 === 0 ? 'استثماري / خاص' : 'خاص',
        classificationEn: idCounter % 3 === 0 ? 'Investment / Private' : 'Private',
        govAr,
        govEn,
        districtAr: districtObj.ar,
        districtEn: districtObj.en,
        regionAr: districtObj.ar,
        regionEn: districtObj.en,
        specialtiesAr: specialtiesObj.ar,
        specialtiesEn: specialtiesObj.en,
        addressAr,
        addressEn,
        phone,
        hotline,
        workingHoursAr: '٢٤ ساعة طوال أيام الأسبوع',
        workingHoursEn: '24/7 Daily'
      });
      usedIds.add(uniqueId);
    }
    idCounter++;
  }
  
  return result;
}

export const privateHospitalsData = generatePrivateHospitals();
